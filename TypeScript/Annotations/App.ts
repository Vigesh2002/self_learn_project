// Annotations

// Annotations are used to specify the data type of a variable, parameter, function return value, and other type of values. Annotations help developers catch errors early in development by allowing them to specify what types of values can be assigned to a given variables or passed as an argument to a function.

// String
let myName: string = "Vigesh";
console.log(myName);

// Number
let favNumber: number = 10;
console.log(favNumber);

// Boolean
let isHard: boolean = true;
console.log(isHard);