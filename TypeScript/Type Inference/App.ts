// Type Inference

// Type inference is a feature in TypeScript that allows the compiler to automatically determine the type of a variable based on its value. In other words, if you declare a variable without explicitly specifying its type, TypeScript will try to infer the based on the value assign to it.

let tech = "TypeScript";
console.log(tech)
console.log(typeof tech)

let favNumber = 10;
console.log(favNumber);
console.log(typeof favNumber);

let isHard = true;
console.log(isHard);
console.log(typeof isHard);